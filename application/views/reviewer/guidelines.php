            <div class="box-header with-border"><h3 class="box-title"><i class="fa fa-book"></i> Review Guideline</h3></div>
            <div class="box-body">
                <div class="alert alert-info">
                    <strong>Goal:</strong> submit one complete recommendation only when you are sure. You can submit again only after editor requests re-review.
                </div>
                <h4>1) Before you start</h4>
                <ul>
                    <li>Confirm no conflict of interest.</li>
                    <li>Check review due date and scope fit.</li>
                    <li>Read manuscript fully before scoring.</li>
                </ul>

                <h4>2) Evaluation criteria</h4>
                <ul>
                    <li>Novelty and contribution to knowledge.</li>
                    <li>Methodological soundness and reproducibility.</li>
                    <li>Results interpretation and references quality.</li>
                    <li>Ethics, plagiarism, and publication integrity.</li>
                    <li>Language clarity and structure.</li>
                </ul>

                <h4>3) Recommendation standard</h4>
                <p>
                    Choose exactly one recommendation: <strong>Accept</strong>, <strong>Minor Review</strong>, <strong>Major Review</strong>, or <strong>Reject</strong>.
                    Provide clear comments to author and confidential comments to editor.
                </p>

                <h4>4) Communication quality</h4>
                <ul>
                    <li>Be specific and constructive (avoid one-line comments).</li>
                    <li>Use numbered points for required fixes.</li>
                    <li>Keep tone professional and unbiased.</li>
                </ul>

                <h4>5) Workflow rule</h4>
                <p>
                    After you submit review comments, you cannot submit another comment on the same assignment until the editor explicitly requests re-review.
                </p>

