                    <h2 style="color: #2c5f2d; margin-top: 30px;">Reviewer Responsibilities</h2>
                    
                    <h3 style="color: #495057;">1. Confidentiality</h3>
                    <p>Manuscripts are confidential materials. Do not share or discuss with anyone without permission.</p>

                    <h3 style="color: #495057;">2. Conflict of Interest</h3>
                    <p>Declare any potential conflicts and recuse yourself if necessary:</p>
                    <ul>
                        <li>Recent collaboration with authors</li>
                        <li>Personal relationships</li>
                        <li>Competing financial interests</li>
                        <li>Same institution as authors</li>
                    </ul>

                    <h3 style="color: #495057;">3. Fairness</h3>
                    <p>Reviews should be honest and objective. Do not be influenced by:</p>
                    <ul>
                        <li>Origin of the manuscript</li>
                        <li>Religious, political, or cultural viewpoints</li>
                        <li>Gender, race, or ethnicity of authors</li>
                    </ul>

                    <h3 style="color: #495057;">4. Timeliness</h3>
                    <p>Accept only if you can complete the review within the deadline. Notify editors promptly if delays occur.</p>

                    <h2 style="color: #2c5f2d; margin-top: 30px;">Review Criteria</h2>
                    
                    <div style="background: #f8fafc; padding: 20px; border-radius: 10px;">
                        <p><strong>Evaluate the manuscript on:</strong></p>
                        <ul>
                            <li><strong>Originality</strong> - Is the research novel and significant?</li>
                            <li><strong>Contribution to field</strong> - Advances agricultural science</li>
                            <li><strong>Technical quality</strong> - Methodology sound and appropriate</li>
                            <li><strong>Clarity of presentation</strong> - Well-written and organized</li>
                            <li><strong>Depth of research</strong> - Thorough investigation</li>
                            <li><strong>Adherence to guidelines</strong> - Follows journal requirements</li>
                            <li><strong>Ethical standards</strong> - Proper approvals and conduct</li>
                        </ul>
                    </div>

