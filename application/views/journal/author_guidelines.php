                    <h3 style="color: #2c5f2d; margin-top: 30px;">1. Manuscript Preparation</h3>
                    
                    <h4 style="color: #495057;">General Format</h4>
                    <ul style="line-height: 1.8;">
                        <li>Use Microsoft Word (DOC/DOCX) or LaTeX</li>
                        <li>Font: Times New Roman, 12 point</li>
                        <li>Line spacing: Single</li>
                        <li>Page numbers and line numbers included</li>
                        <li>Margins: 2.54 cm (1 inch) on all sides</li>
                    </ul>

                    <h4 style="color: #495057; margin-top: 20px;">Title Page</h4>
                    <ul style="line-height: 1.8;">
                        <li>Brief title (maximum 20 words)</li>
                        <li>Full names of all authors</li>
                        <li>Affiliations (Department, Institution, City, Country)</li>
                        <li>Corresponding author with email</li>
                        <li>ORCID IDs (recommended)</li>
                    </ul>

                    <h4 style="color: #495057; margin-top: 20px;">Abstract</h4>
                    <ul style="line-height: 1.8;">
                        <li>Research Articles: 250 words maximum</li>
                        <li>Review Articles: 300 words maximum</li>
                        <li>Short Communications: 200 words maximum</li>
                        <li>Should include: Objective, Methods, Results, Conclusion</li>
                    </ul>

                    <h4 style="color: #495057; margin-top: 20px;">Keywords</h4>
                    <ul style="line-height: 1.8;">
                        <li>5-8 keywords</li>
                        <li>Separated by dashes</li>
                    </ul>

                    <h3 style="color: #2c5f2d; margin-top: 30px;">2. Manuscript Structure</h3>
                    
                    <div style="background: #f8fafc; padding: 20px; border-radius: 10px; margin-bottom: 20px;">
                        <h4 style="color: #2c5f2d;">Research Articles</h4>
                        <ol style="line-height: 1.8;">
                            <li><strong>Introduction</strong> - Background, rationale, objectives</li>
                            <li><strong>Materials and Methods</strong> - Detailed methodology</li>
                            <li><strong>Results</strong> - Findings with tables and figures</li>
                            <li><strong>Discussion</strong> - Interpretation and comparison</li>
                            <li><strong>Conclusion</strong> - Key takeaways</li>
                            <li><strong>Acknowledgments</strong></li>
                            <li><strong>References</strong> - Vancouver style</li>
                        </ol>
                    </div>

                    <h3 style="color: #2c5f2d; margin-top: 30px;">3. Figures and Tables</h3>
                    
                    <div class="row">
                        <div class="col-md-6">
                            <div class="box" style="border: 1px solid #2c5f2d; border-radius: 10px; padding: 15px;">
                                <h4 style="color: #2c5f2d; margin-top: 0;">Figures</h4>
                                <ul style="padding-left: 20px;">
                                    <li>Resolution: 300 DPI minimum</li>
                                    <li>Formats: TIFF, EPS, PDF, JPEG</li>
                                    <li>Width: 8-16 cm</li>
                                    <li>Color: RGB mode</li>
                                </ul>
                            </div>
                        </div>
                        <div class="col-md-6">
                            <div class="box" style="border: 1px solid #2c5f2d; border-radius: 10px; padding: 15px;">
                                <h4 style="color: #2c5f2d; margin-top: 0;">Tables</h4>
                                <ul style="padding-left: 20px;">
                                    <li>Editable format (Word)</li>
                                    <li>Caption above the table</li>
                                    <li>Avoid shading or colors</li>
                                    <li>Units in parentheses</li>
                                </ul>
                            </div>
                        </div>
                    </div>

